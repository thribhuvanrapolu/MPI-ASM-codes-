ASSUME DS:DATA, CS:CODE
DATA SEGMENT
STR1 db 0AH,0DH,"First 1-digit number:", '$';
STR2 db 0AH,0DH,"Second 1-digit number:",'$';
STR3 db 0AH,0DH,"Sum is: ", '$';
DATA ENDS
CODE SEGMENT
START:
MOV AX, DATA
MOV DS, AX

MOV AH, 09H
LEA DX, STR1
INT 21H

MOV AH, 01H
INT 21H

SUB AL, '0'
MOV CL, AL

MOV AH, 09H
LEA DX, STR2
INT 21H

MOV AH, 01H
INT 21H

MOV AH, 09H
LEA DX, STR3
INT 21H

SUB AL, '0';
ADD CL, AL;

ADD CL, '0'
MOV DL,CL
MOV AH, 02H
INT 21H

MOV AH, 4CH
INT 21H

CODE ENDS
END START
